"""
membership operator ( in ) is used to check if an object exists in a sequence
"""

fruit = "apple"
print("a" in fruit)

c1 = ["jc","ac","bc","cc"] # we use [] square brackets to indicate the use of a list
print("bc" in c1)

c2 = [10,20,30,40]
print(30 in c2)

c3 = [3.12,4.23,5.34]
print(1.23 in c3)
